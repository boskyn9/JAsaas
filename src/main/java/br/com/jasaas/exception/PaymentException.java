package br.com.jasaas.exception;

/**
 *
 * @author willian
 */
public class PaymentException extends RuntimeException {

    private int code;

    public PaymentException(int code, String message) {
        super(message);
        this.code = code;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

}
